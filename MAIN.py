PIXEL_SPACING_MM = 0.2  # Adjust if calibrated

# --- Classification thresholds ---
def classify_treatment(area_mm2):
    if area_mm2 < 50:
        return "Conservative Treatment"
    elif area_mm2 < 200:
        return "Intramedullary Nailing"
    else:
        return "Open Reduction and Internal Fixation (ORIF)"

# --- Load mask from COCO ---
def get_mask(filename):
    matched_info = next((info for info in images_info if filename in info['file_name']), None)
    if matched_info is None:
        return None, None
    mask = np.zeros((matched_info['height'], matched_info['width']), dtype=np.uint8)
    ann_ids = coco.getAnnIds(imgIds=matched_info['id'])
    anns = coco.loadAnns(ann_ids)
    for ann in anns:
        rle = coco.annToRLE(ann)
        m = maskUtils.decode(rle)
        mask = np.maximum(mask, m)
    return mask, matched_info

# --- Display image with overlay ---
def display_case(filename, mask, treatment, area_pixels, area_mm2):
    img_path = os.path.join(IMAGES_DIR, filename)
    img = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE)
    img = cv2.resize(img, (256, 256)) / 255.0
    mask = cv2.resize(mask, (256, 256), interpolation=cv2.INTER_NEAREST)
    mask_bin = (mask > 0).astype(np.uint8)

    img_disp = (img * 255).astype(np.uint8)
    mask_disp = (mask_bin * 255).astype(np.uint8)
    overlay = np.stack([img_disp]*3, axis=-1)
    overlay[..., 0] = np.maximum(overlay[..., 0], mask_disp)

    plt.figure(figsize=(5, 5))
    plt.imshow(overlay)
    plt.title(f"{treatment}\nFracture Size: {area_pixels} pixels ≈ {area_mm2:.2f} mm²")
    plt.axis('off')
    plt.show()

# --- Scan full dataset until all three categories are found ---
found = {}
for info in images_info:
    filename = info['file_name']
    mask, matched_info = get_mask(filename)
    if mask is None:
        continue
    mask_bin = (cv2.resize(mask, (256, 256), interpolation=cv2.INTER_NEAREST) > 0).astype(np.uint8)
