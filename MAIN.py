def ResUNet(input_shape=(256, 256, 1)):
    inputs = layers.Input(input_shape)

    def conv_block(x, filters):
        res = layers.Conv2D(filters, 3, padding='same')(x)
        res = layers.BatchNormalization()(res)
        res = layers.ReLU()(res)
        res = layers.Conv2D(filters, 3, padding='same')(res)
        res = layers.BatchNormalization()(res)
        shortcut = layers.Conv2D(filters, 1, padding='same')(x)
        shortcut = layers.BatchNormalization()(shortcut)
        out = layers.Add()([res, shortcut])
        return layers.ReLU()(out)

    # Encoder
    c1 = conv_block(inputs, 64)
    p1 = layers.MaxPooling2D()(c1)
    c2 = conv_block(p1, 128)
    p2 = layers.MaxPooling2D()(c2)
    c3 = conv_block(p2, 256)
    p3 = layers.MaxPooling2D()(c3)

    # Bottleneck
    b = conv_block(p3, 512)

    # Decoder
    u3 = layers.UpSampling2D()(b)
    u3 = layers.Concatenate()([u3, c3])
    c4 = conv_block(u3, 256)
    u2 = layers.UpSampling2D()(c4)
    u2 = layers.Concatenate()([u2, c2])
    c5 = conv_block(u2, 128)
    u1 = layers.UpSampling2D()(c5)
